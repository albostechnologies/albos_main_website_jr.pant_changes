export async function POST() {
  try {
    let created = 0;
    let skipped = 0;

    for (const job of SEED_JOBS) {
      const existing = await db.jobListing.findUnique({
        where: { slug: job.slug },
      });

      if (existing) {
        skipped++;
        continue;
      }

      await db.jobListing.create({ data: job });
      created++;
    }

    const total = await db.jobListing.count();

    return NextResponse.json({
      success: true,
      message: `Seeded ${created} jobs, skipped ${skipped} existing. Total: ${total}`,
      created,
      skipped,
      total,
    });
  } catch (error) {
    console.error("Job seed error:", error);
    return NextResponse.json(
      { success: false, error: "Failed to seed jobs" },
      { status: 500 },
    );
  }
}

function parseJsonArray(value) {
  try {
    return JSON.parse(value);
  } catch {
    return [];
  }
}

const POSTED_AT = "2024-11-01T00:00:00.000Z";

export function getPublishedJobListings() {
  return SEED_JOBS_RAW.filter((job) => job.published).map((job) => ({
    id: job.slug,
    title: job.title,
    slug: job.slug,
    department: job.department,
    location: job.location,
    type: job.type,
    experience: job.experience,
    salary: job.salary ?? null,
    description: job.description,
    requirements: parseJsonArray(job.requirements),
    benefits: parseJsonArray(job.benefits),
    skills: parseJsonArray(job.skills),
    featured: job.featured,
    published: job.published,
    postedAt: POSTED_AT,
    createdAt: POSTED_AT,
    updatedAt: POSTED_AT,
  }));
}
